import { artistsData } from "./artistsData";
import { organizationsData } from "./organizationsData";

export interface ProjectTag {
  text: string;
  hasIcon: boolean;
}

export interface ProjectCharacteristic {
  name: string;
  description: string;
}

export interface ProjectDetails {
  title: string;
  tags: ProjectTag[];
  links: {
    saveArt: string;
    artUa: string;
  };
  slides: string[];
  initialLikes: number;
  characteristicsTitle: string;
  tableHeaders: {
    name: string;
    description: string;
  };
  characteristics: ProjectCharacteristic[];
}

export interface ProjectDescriptionData {
  slides: string[];
  tags: string[];
  date: string;
  title: string;
  aboutAuthor: {
    avatar: string;
    name: string;
    description: string;
    artUaLink: string;
    saveArtLink: string;
  };
  socialLinks: Array<{
    icon: string;
    alt: string;
  }>;
  descriptionText: string[];
}

export interface Project {
  id: number;
  authorId: number;
  image: string;
  title: string;
  slug: string;
  date: string;
  artSubCategory: string;
  likes: number;
  authorAvatar: string;
  authorName: string;
  projectDetails: ProjectDetails;
  projectDescriptionData: ProjectDescriptionData;
}

const authorById = new Map(
  [...artistsData, ...organizationsData].map((author) => [author.id, author]),
);

const getProjectAuthor = (projectId: number) => {
  const artist = authorById.get(projectId);

  return {
    authorAvatar: artist?.artistPhoto ?? "",
    authorName: artist?.artistName ?? "",
  };
};

const getProjectAboutAuthor = (authorId: number): ProjectDescriptionData["aboutAuthor"] => {
  const artist = authorById.get(authorId);
  const slug = artist?.slug ?? "";

  return {
    avatar: artist?.artistPhoto ?? "",
    name: artist?.artistName ?? "",
    description: artist?.artistType ?? "",
    artUaLink: slug ? `art-ua.info/${slug}` : "",
    saveArtLink: slug ? `save-art.in.ua/${slug}` : "",
  };
};

